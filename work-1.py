# Görev 1:  Verilen değerlerin veri yapılarını inceleyiniz.Type() metodunu kullanınız.

x = 8
type(x)
# Out = int

y = 3.2
type(y)
# Out = float

z = 8j + 18
type(z)
# Out = complex

b = True
type(b)
# Out = bool

c = 23 < 22
type(c)
# Out = bool

l = [1, 2, 3, 4]
type(l)
# Out = tuple

d = {"Name":"Jake", "Age":27, "Adress":"Downtown" }
type(d)
# Out = dict

s = {"Python", "Machine Learning", "Data Science"}
type(s)
# Out = Set
